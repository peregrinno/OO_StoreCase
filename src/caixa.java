class Caixa {

    Float caixa_inicial;
    String operador;
    int resgis_caixa;

    public void setCaixaInicial(Float caixa_inicial) {
        this.caixa_inicial = caixa_inicial;
    }

    public Float getCaixaInicial() {
        return caixa_inicial;
    }

    public void setOperador(String operador) {
        this.operador = operador;
    }

    public String getOperador() {
        return operador;
    }

    public void setResgis_Caixa(int re_caixa) {
        this.resgis_caixa = re_caixa;
    }

    public Integer getResgis_Caixa(){
        return resgis_caixa;
    }

}
